package cz.cvut.omo.sp.alive.inhabitant;

/**
 * Enum representing the possible types of inhabitants in a simulation.
 *
 * This enum defines the various types that an inhabitant can be in the simulation,
 * including PET and PERSON.
 */
public enum TypeInhabitant {
    /** The inhabitant is a pet. */
    PET,

    /** The inhabitant is a person. */
    PERSON;
}
