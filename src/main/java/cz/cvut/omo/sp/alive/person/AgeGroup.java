package cz.cvut.omo.sp.alive.person;

/**
 * Enum representing the possible age groups of individuals in a simulation.
 *
 * This enum defines various age groups that individuals can belong to in the simulation,
 * including CHILD, ADULT, BABY, STUDENT, and AGED.
 */
public enum AgeGroup {
    /** The individual is a child. */
    CHILD,

    /** The individual is an adult. */
    ADULT,

    /** The individual is a baby. */
    BABY,

    /** The individual is a student. */
    STUDENT,

    /** The individual is aged. */
    AGED;
}
