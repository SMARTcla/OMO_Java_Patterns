package cz.cvut.omo.sp.alive.pet;

/**
 * Enum representing the possible types of pets in a simulation.
 *
 * This enum defines the various types that a pet can be in the simulation,
 * including CAT, DOG, FISH, HAMSTER, LION, and PARROT.
 */
public enum TypePet {
    /** The pet is a cat. */
    CAT,

    /** The pet is a dog. */
    DOG,

    /** The pet is a fish. */
    FISH,

    /** The pet is a hamster. */
    HAMSTER,

    /** The pet is a lion. */
    LION,

    /** The pet is a parrot. */
    PARROT;
}
